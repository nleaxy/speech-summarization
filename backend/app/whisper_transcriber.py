# ======================================================================
# НОВОЕ: Пресеты `initial_prompt` для распознавания речи на
# специализированные темы.
#
# Зачем это нужно:
#   Whisper поддерживает нативный параметр `initial_prompt` — короткий
#   текст-подсказку, который задаёт контекст и словарь домена.
#   Это улучшает распознавание редких терминов (например "миокардит",
#   "апелляционная инстанция", "гидродинамика") БЕЗ смены или
#   дообучения модели.
#
# Как это работает:
#   Пользователь (или вызывающий код) передаёт `domain="medical"`,
#   и мы подставляем соответствующий промт в `model.transcribe`.
#   По умолчанию (`domain="default"` или None) промт не передаётся —
#   поведение полностью совпадает с прежним (обратная совместимость).
# ======================================================================
#
# ВАЖНО про формат промтов:
#   Whisper `initial_prompt` работает как "скользящее окно контекста" —
#   модель считает, что это речь, которая звучала ДО распознаваемого
#   фрагмента. Поэтому промт должен быть ТОЛЬКО перечислением
#   терминов/словаря домена, БЕЗ описательных фраз вида
#   "Ниже приведена запись...". Иначе подобные конструкции могут
#   "утечь" в распознанный текст и исказить транскрипт.
#
#   Цель: дать модели подсказку по редкой лексике, чтобы она лучше
#   РАСПОЗНАВАЛА её, не изменяя смысл и стиль самого текста.
#
DOMAIN_PROMPTS = {
    # Научная лексика: физика, химия, биология, математика
    "scientific": (
        "гипотеза, эксперимент, теорема, квантовая механика, "
        "термодинамика, энтропия, гидродинамика, электромагнетизм, "
        "митохондрия, фермент, катализатор, ДНК, РНК, геном, "
        "дифференциальное уравнение, интеграл, матрица, алгоритм, "
        "нейрон, изотоп, фотон, гравитация."
    ),
    # Медицинская лексика: диагнозы, препараты, анатомия
    "medical": (
        "анамнез, диагноз, симптом, этиология, патогенез, "
        "гипертония, миокардит, тахикардия, аритмия, ишемия, "
        "антибиотик, антисептик, анальгетик, антикоагулянт, "
        "МРТ, КТ, УЗИ, ЭКГ, ЭЭГ, рентгенография, "
        "педиатрия, кардиология, неврология, онкология, "
        "пациент, рецепт, дозировка, анестезия."
    ),
    # Юридическая лексика: судопроизводство, нормативные акты
    "legal": (
        "истец, ответчик, иск, ходатайство, апелляция, кассация, "
        "прокурор, адвокат, нотариус, судья, присяжные, "
        "гражданский кодекс, уголовный кодекс, арбитражный суд, "
        "договор, нормативный акт, правоотношение, юрисдикция, "
        "презумпция невиновности, состав преступления, "
        "правоспособность, дееспособность, правонарушение."
    ),
    # Пустой промт = прежнее поведение
    "default": None,
}


def _resolve_initial_prompt(domain):
    """
    Возвращает текст initial_prompt для заданного домена или None,
    если домен не задан / неизвестен (fallback на поведение по умолчанию).
    """
    if not domain:
        return None
    return DOMAIN_PROMPTS.get(domain.lower(), None)


def _add_speaker_diarization(transcription):
    """Приватная функция для эмуляции распределения голосов."""
    speakers = ["SPEAKER_01", "SPEAKER_02"]
    current_speaker = speakers[0]
    
    for i, segment in enumerate(transcription.get("segments", [])):
        if i > 0 and i % 2 == 0:
            current_speaker = speakers[1] if current_speaker == speakers[0] else speakers[0]
        segment["speaker"] = current_speaker
    
    return transcription

# ======================================================================
# ЭТО НАША ГЛАВНАЯ ФУНКЦИЯ, КОТОРУЮ БУДЕТ ДЕРГАТЬ БЭКЕНД
# ======================================================================
def run_transcription(audio_filepath: str, summary_type: str, domain: str = "default"):
    """
    Принимает путь к аудиофайлу и возвращает результат транскрибации в виде словаря.

    Параметры:
        audio_filepath: путь к аудиофайлу.
        summary_type:   тип суммаризации (передаётся дальше в summarizer).
        domain:         ОПЦИОНАЛЬНО. Доменный пресет для Whisper `initial_prompt`.
                        Поддерживаемые значения: "scientific", "medical",
                        "legal", "default". По умолчанию — "default"
                        (промт не используется, поведение прежнее).
                        Добавлено для улучшения распознавания специализированной
                        лексики без смены модели.
    """
    try:
        print(f"INFO: Начинаю обработку файла: {audio_filepath}")

        # 1. Загрузка аудио
        audio_data, _ = librosa.load(audio_filepath, sr=16000)
        audio_data = audio_data.astype(np.float32)

        # 2. Транскрибация
        # ИЗМЕНЕНО: если задан domain — передаём соответствующий initial_prompt
        # в Whisper. Если промт None — вызываем transcribe как раньше,
        # чтобы сохранить 100% обратную совместимость со старой сигнатурой.
        initial_prompt = _resolve_initial_prompt(domain)
        if initial_prompt:
            print(f"INFO: Применяю доменный пресет '{domain}' (initial_prompt).")
            result = model.transcribe(audio_data, initial_prompt=initial_prompt)
        else:
            result = model.transcribe(audio_data)
        
        # 3. Добавление "говорящих" (диаризация)
        result_with_speakers = _add_speaker_diarization(result)

        # 4. Формируем красивый итоговый JSON для ответа API
        final_result = {
            "text": result_with_speakers.get("text", "").strip(),
            "language": result_with_speakers.get("language", ""),
            "segments": [
                {
                    "start": segment.get("start"),
                    "end": segment.get("end"),
                    "text": segment.get("text", "").strip(),
                    "speaker": segment.get("speaker")
                }
                for segment in result_with_speakers.get("segments", [])
            ]
        }

        # --- ЭТАП 2: СУММАРИЗАЦИЯ ---
        # Теперь, когда у нас есть текст, мы передаем его в суммаризатор.
        
        # Проверяем, что текст не пустой
        if final_result and final_result.get("text"):
            print(f"INFO: Текст получен, отправляю на суммаризацию (тип: {summary_type})...")
            
            # Вызываем функцию summarize_text из соседнего файла
            summary_data = summarize_text(final_result["text"], summary_type)
            
            # Добавляем результат суммаризации в наш итоговый JSON
            final_result["summary_data"] = summary_data
        else:
            # Если текста нет, добавляем сообщение об ошибке
            final_result["summary_data"] = {"error": "Текст для суммаризации не был получен."}
        
        print(f"INFO: Обработка файла {audio_filepath} завершена успешно.")
        return final_result
        
    except Exception as e:
        print(f"ERROR: Произошла ошибка при обработке файла: {e}")
        # В случае ошибки возвращаем None, чтобы бэкенд понял, что что-то пошло не так
        return None